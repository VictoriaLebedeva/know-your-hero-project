class ErrorCodes:
    # Auth errors (1000-1099)
    INVALID_CREDENTIALS = 1001
    TOO_MANY_ATTEMPTS = 1002
    MISSING_FIELDS = 1004
    EMAIL_EXISTS = 1010
    ACCOUNT_LOCKED = 1011
    USER_NOT_FOUND = 1013
    INVALID_EMAIL_FORMAT = 1014

    # Token errors (1020-1039)
    MISSING_TOKEN = 1020
    INVALID_TOKEN = 1021
    EXPIRED_TOKEN = 1022
    TOKEN_NOT_FOUND = 1023
    TOKEN_ALREADY_REVOKED = 1024
    TOKEN_VALIDATION_ERROR = 1025

    # Permission errors (1040-1099)
    INSUFFICIENT_PERMISSIONS = 1040

    # Review errors (4000-4099)
    REVIEW_TARGET_NOT_FOUND = 4001
    SELF_REVIEW_NOT_ALLOWED = 4002

    # Server errors (5000-5099)
    SERVER_ERROR = 5001
    DATABASE_ERROR = 5002
